bool render_wpm(void) {
	for (uint8_t p = 0; p < 4; p++) {
        oled_set_cursor(9, p);
        oled_write_raw_P(wpm + p * 8, 8);
    }
	
    // 1) Get the current WPM (0–255)
    uint8_t wpm = get_current_wpm();

    // 2) Split into digits
    uint8_t d_hund = wpm / 100;
    uint8_t d_tens = (wpm / 10) % 10;
    uint8_t d_ones = wpm % 10;

    // 3) Decide how many digits to draw (1–3)
    uint8_t num_digits = (wpm >= 100 ? 3 : (wpm >= 10 ? 2 : 1));

    // 4) Bottom‐align across the 4 pages (0–3)
    //    start_page + (0…num_digits–1) → pages 4–num_digits … 3
    uint8_t start_page = 4 - num_digits;

    // 5) X‐position (in characters/pages, here 0 = leftmost 8px block)
    const uint8_t x_col = 7;

    // 6) Loop through each digit slot
    for (uint8_t i = 0; i < num_digits; i++) {
        // pick the right digit
        uint8_t digit = (i == 0 && num_digits == 3) ? d_hund
                       : (i == 1 && num_digits >= 2) ? d_tens
                       : d_ones;

        // pull the PROGMEM pointer
        const char *bm = (const char *)pgm_read_ptr(&digit_frames[digit]);

        // position cursor to the correct page
        oled_set_cursor(x_col, start_page + i);

        // write 8 raw bytes (one full 8×8 block)
        oled_write_raw_P(bm, 8);
    }

	return false;
}

bool render_layer_indicators(void) {
    oled_clear();

    // —— Which layer is live?
    uint8_t active = get_highest_layer(layer_state);

    // —— One-shot state (per layer)
    static int8_t  prev_active = -1;
    static uint8_t anim_idx[4];
    static uint32_t anim_timer[4];
    static bool    anim_done[4];

    // —— Reset if we just switched to a new layer
    if (active != prev_active) {
        anim_idx[active]    = 0;
        anim_timer[active]  = 0;
        anim_done[active]   = false;
        prev_active         = active;
    }

    // —— Per-layer frame data
    static const char * const * layer_frames[4] = {
        base_frames,
        num_frames,
        nav_frames,
        func_frames
    };
    static const char * idle_frame0[4] = {
        base_0,
        num_0,
        nav_0,
        func_0
    };
    static const uint8_t layer_cols[4] = {
        18,  // Base
        16,  // Num
        14,  // Nav
        12   // Func
    };

    // —— Draw each slot
    for (uint8_t layer = 0; layer < 4; layer++) {
        const char *bm;
        uint8_t     col = layer_cols[layer];

        if (layer == active) {
            // — Active layer: pick & advance animation frame
            uint8_t idx = anim_idx[layer] < LAYER_FRAME_COUNT
                        ? anim_idx[layer]
                        : (LAYER_FRAME_COUNT - 1);
            bm = (const char *)pgm_read_ptr(&layer_frames[layer][idx]);

            if (!anim_done[layer]) {
                if (anim_timer[layer] == 0) {
                    anim_timer[layer] = timer_read();
                }
                if (timer_elapsed(anim_timer[layer]) >= ANIM_FRAME_MS) {
                    anim_timer[layer] = timer_read();
                    anim_idx[layer]++;
                    if (anim_idx[layer] >= LAYER_FRAME_COUNT) {
                        anim_idx[layer]   = LAYER_FRAME_COUNT - 1;
                        anim_done[layer]  = true;
                    }
                }
            }
        } else {
            // — Idle layer: show frame 0
            bm = idle_frame0[layer];
        }

        // — Draw the 11×32 bitmap upright at (col, top)
        for (uint8_t page = 0; page < LAYER_FRAME_PAGES; page++) {
            oled_set_cursor(col, page);
            oled_write_raw_P(
                bm + page * LAYER_FRAME_WIDTH,
                LAYER_FRAME_WIDTH
            );
        }
    }

    return false;
}

bool render_modifier_indicators(void) {
    static const char * const *mod_frames[4] = { ctrl_frames, alt_frames, shift_frames, win_frames };
    static const char * const  mod_idle0[4]  = { ctrl_0,     alt_0,     shift_0,     win_0        };
    static const uint8_t       mod_mask[4]   = { MOD_MASK_CTRL, MOD_MASK_ALT, MOD_MASK_SHIFT, MOD_MASK_GUI };
    static const uint8_t       mod_col[4]       = { 3, 0,  3, 0 };
    static const uint8_t       mod_page_base[4] = { 0, 0,  2, 2 };

    static uint8_t  anim_idx[4]   = {0};
    static uint32_t anim_timer[4] = {0};
    static bool     anim_done[4]  = {false};

    uint8_t mods       = get_mods();
    uint8_t oneshots   = get_oneshot_mods();
    led_t   led_state  = host_keyboard_led_state();
    bool    caps_on    = led_state.caps_lock;

    // Treat Shift as “active” if held, oneshot, or Caps Lock is on.
    bool shift_active = ((mods | oneshots) & MOD_MASK_SHIFT) || caps_on;

    for (uint8_t i = 0; i < 4; i++) {
        const char *bm;
        bool active = (i == 2) ? shift_active : ((mods & mod_mask[i]) != 0);

        if (active) {
            uint8_t idx = anim_idx[i] < MOD_FRAME_COUNT ? anim_idx[i] : (MOD_FRAME_COUNT - 1);
            bm = (const char *)pgm_read_ptr(&mod_frames[i][idx]);

            if (!anim_done[i]) {
                if (anim_timer[i] == 0) anim_timer[i] = timer_read();
                if (timer_elapsed(anim_timer[i]) >= ANIM_FRAME_MS) {
                    anim_timer[i] = timer_read();
                    anim_idx[i]++;
                    if (anim_idx[i] >= MOD_FRAME_COUNT) {
                        anim_idx[i]  = MOD_FRAME_COUNT - 1;
                        anim_done[i] = true;
                    }
                }
            }
        } else {
            bm = mod_idle0[i];
            anim_idx[i]   = 0;
            anim_timer[i] = 0;
            anim_done[i]  = false;
        }

        uint8_t col = mod_col[i];
        uint8_t base = mod_page_base[i];
        for (uint8_t p = 0; p < MOD_FRAME_PAGES; p++) {
            oled_set_cursor(col, base + p);
            oled_write_raw_P(bm + p * MOD_FRAME_WIDTH, MOD_FRAME_WIDTH);
        }
    }
    return false;
}

